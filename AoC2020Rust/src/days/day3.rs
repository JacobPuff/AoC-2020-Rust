pub fn day3() {
    // Part one
    let part_one_count: usize = trees_on_slope(3, 1);
    println!("There are {} trees in part one.", part_one_count);

    // Part two
    let multiplied_slopes: usize =
        trees_on_slope(1, 1) *
        part_one_count *
        trees_on_slope(5, 1) *
        trees_on_slope(7, 1) *
        trees_on_slope(1, 2);
    println!("Part two's slope counts multiplied is {}", multiplied_slopes);
}

fn trees_on_slope(right: usize, down: usize) -> usize {
    let data = get_data();
    let mut x: usize = 0;
    let mut y: usize = 0;
    let width = 31;
    let height = data.len();
    let mut num_of_trees: usize = 0;
    while y < height {
        if data[y].chars().nth(x % width).unwrap() == '#' {
            num_of_trees += 1;
        }
        y += down;
        x += right;
    }
    num_of_trees
}

